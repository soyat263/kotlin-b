package `Kotlin Operator`

fun main(){
    //Addition Operatar
    var a = 120
    var b = 65.0
    var sum = a+b
    println("Sum:$sum")


    //Substraction Operator

    var sub = a-b
    println("Sub:$sub")

    //Multiplication Operator

    var mult = a*b.toDouble()
    println("Mult:$mult")


    //Division Operator

    var div = a/b
    println("Div:$div")

    //Modulus Operator

    var mud = a%b
    println("Mud:$mud")
}